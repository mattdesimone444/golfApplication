package com.furious.golf.service.util;

public class GolfOptoSettings {
    int numberLineups;
    int maxOwnershipPer;
    int minOwnershipPer;
    int maxIndividualOwnerPer;
    int minIndividualOwnerPer;
    int minSalary;
    int maxSalary;
    int uniquePlayers;
    int globalExposure;

    public int getNumberLineups() {
        return numberLineups;
    }

    public void setNumberLineups(int numberLineups) {
        this.numberLineups = numberLineups;
    }

    public int getMaxOwnershipPer() {
        return maxOwnershipPer;
    }

    public void setMaxOwnershipPer(int maxOwnershipPer) {
        this.maxOwnershipPer = maxOwnershipPer;
    }

    public int getMinOwnershipPer() {
        return minOwnershipPer;
    }

    public void setMinOwnershipPer(int minOwnershipPer) {
        this.minOwnershipPer = minOwnershipPer;
    }

    public int getMaxIndividualOwnerPer() {
        return maxIndividualOwnerPer;
    }

    public void setMaxIndividualOwnerPer(int maxIndividualOwnerPer) {
        this.maxIndividualOwnerPer = maxIndividualOwnerPer;
    }

    public int getMinIndividualOwnerPer() {
        return minIndividualOwnerPer;
    }

    public void setMinIndividualOwnerPer(int minIndividualOwnerPer) {
        this.minIndividualOwnerPer = minIndividualOwnerPer;
    }

    public int getMinSalary() {
        return minSalary;
    }

    public void setMinSalary(int minSalary) {
        this.minSalary = minSalary;
    }

    public int getMaxSalary() {
        return maxSalary;
    }

    public void setMaxSalary(int maxSalary) {
        this.maxSalary = maxSalary;
    }

    public int getUniquePlayers() {
        return uniquePlayers;
    }

    public void setUniquePlayers(int uniquePlayers) {
        this.uniquePlayers = uniquePlayers;
    }

    public int getGlobalExposure() {
        return globalExposure;
    }

    public void setGlobalExposure(int globalExposure) {
        this.globalExposure = globalExposure;
    }
}
